/** Deterministic fallback colour for symbols with no brand entry. */
function hashColor(sym: string): string {
  let h = 0;
  for (const c of sym) h = c.charCodeAt(0) + ((h << 5) - h);
  return `hsl(${Math.abs(h) % 360}, 55%, 50%)`;
}

