/**
 * A colour is only an identity if you can see it: anything darker than the UI background gets its
 * lightness floored (hue kept). Catches the remaining near-black brands (XRP, Nike, Palantir…)
 * without rewriting their entries.
 */
function visibleColor(hex: string): string {
  if (!hex.startsWith('#') || hex.length !== 7) return hex;
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;
  const max = Math.max(r, g, b), min = Math.min(r, g, b);
  const l = (max + min) / 2;
  if (l >= 0.18) return hex;
  let h = 0, s = 0;
  if (max !== min) {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    if (max === r) h = ((g - b) / d + (g < b ? 6 : 0)) / 6;
    else if (max === g) h = ((b - r) / d + 2) / 6;
    else h = ((r - g) / d + 4) / 6;
  }
  return `hsl(${Math.round(h * 360)}, ${Math.round(s * 100)}%, 32%)`;
}

export function getAssetColor(symbol: string): string {
  return visibleColor(CRYPTO_ICON_COLORS[symbol] || BRAND_COLORS[symbol] || hashColor(symbol));
}

